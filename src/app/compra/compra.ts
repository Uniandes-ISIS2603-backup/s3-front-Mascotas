/**
 * Clase que representa la compra de una mascota
 */

 export class Compra{

    /**
     * Representa el identificador de la compra.
     */
    mascotaId: number;

    /**
     * Representa el precio con el que se compro una mascota.
     */
    precio: number;

  /**
   * Representa el tipo de pago con el que se compro una mascota
   */
  tipoDePago: string;
 }
